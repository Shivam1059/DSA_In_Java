
import java.util.Scanner;

class DigitSum{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.err.println("Enter the a : ");
        int a = sc.nextInt();
        System.err.println("Enter the b : ");
        int b = sc.nextInt();

        int sum = a+b;
        System.out.println("Sum : "+sum);

    }
}